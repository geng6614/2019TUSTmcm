function plot_Normality(Time,n)
%--------------------------------------------------------------------
%函数功能：验证正态分布
%创建时间：2019年6月10日
%入口参数：随机数方阵大小n
%出口参数：无
%--------------------------------------------------------------------

for i = 1:Time
    C = Get_data(n);
    x = C(:,1);
    y = C(:,2);
    D(:,1) = x;
    D=[D,y];
end
D(:,1)=[];%删除第一列
[m,~] = size(y) ;
if i <=m
    y = mean(D,2);
    i=i+1;
end
y = 1./y;
ylim([0, 0.15]);
plot(x,y);